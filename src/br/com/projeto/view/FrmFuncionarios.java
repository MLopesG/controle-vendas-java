    public void listar() {
        FuncionarioDao dao = new FuncionarioDao();
        List<Funcionario> lista = dao.listarFuncionarios();

        DefaultTableModel dados = (DefaultTableModel) tableClientes.getModel();
        dados.setNumRows(0);

        for (Funcionario c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getSenha(),
                c.getNivel_acesso(),
                c.getCargo()
            });
        }
    }
    
    private void buscarClienteNome(String nome) {
        FuncionarioDao dao = new FuncionarioDao();
        List<Funcionario> lista = dao.buscaClientePorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) tableClientes.getModel();
        dados.setNumRows(0);

        for (Funcionario c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getSenha(),
                c.getCargo(),
                c.getNivel_acesso()
            });
        }
    }
    
