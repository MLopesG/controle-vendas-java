    public void listar() {
        FornecedorDao dao = new FornecedorDao();
        List<Fornecedor> lista = dao.listarFornecedores();

        DefaultTableModel dados = (DefaultTableModel) tableFornecedor.getModel();
        dados.setNumRows(0);

        for (Fornecedor c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCnpj(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado()
            });
        }
    }

