    public void listar() {
        EmailDao dao = new EmailDao();
        List<Email> lista = dao.listarEmails();

        DefaultTableModel dados = (DefaultTableModel) tablesEmails.getModel();
        dados.setNumRows(0);

        for (Email c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getEmail(),
                c.getTitulo(),
                c.getTipo()
            });
        }
    }
    
