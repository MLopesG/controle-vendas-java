    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:

        Utilitarios util = new Utilitarios();

        try {
            if (txtEmail.getText().isEmpty()) {
                util.alert("Sistema de controle PDV - Atenção", "Informe identificação do cadastro, para realizar pesquisa ! ");
            } else {
                EmailDao emailDao = new EmailDao();
                String nome = "%" + txtEmail.getText() + "%";

                if ("Cliente".equals(cbTipo.getSelectedItem().toString())) {
                    Cliente obj = new Cliente();
                    obj = emailDao.consultarEmailCliente(nome);
                    txtEmail.setText(obj.getEmail());
                    txtNome.setText(obj.getNome());
                } else if ("Funcionario".equals(cbTipo.getSelectedItem().toString())) {
                    Funcionario obj = new Funcionario();
                    obj = emailDao.consultarEmailFuncionario(nome);
                    txtEmail.setText(obj.getEmail());
                    txtNome.setText(obj.getNome());
                } else {
                    Fornecedor obj = new Fornecedor();
                    obj = emailDao.consultarEmailFornecedor(nome);
                    txtEmail.setText(obj.getEmail());
                    txtNome.setText(obj.getNome());
                }
            }
        } catch (Exception e) {
            util.alert("Sistema de controle PDV - Atenção", "Error: " + e);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

